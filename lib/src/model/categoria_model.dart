class Categoria {
  String nombre_categoria;
  String catCode;

  setCategoria(String Nombre_categoria) {
    this.nombre_categoria = Nombre_categoria;
  }

  getCategoria() {
    return this.nombre_categoria;
  }

  setCatCode(String catCode) {
    this.catCode = catCode;
  }

  getCatCode() {
    return this.catCode;
  }
}
